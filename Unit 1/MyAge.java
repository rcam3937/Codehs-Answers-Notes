public class MyAge
{
    public static void main(String[] args)
    {
        // Start here!
        
        int age = 14;
        
        System.out.print("I am currently: ");
        System.out.println(age);
        
        age++;
        
        System.out.print("My age next year will be: ");
        System.out.println(age);
        
        age--;
        
        System.out.print("My current age is: ");
        System.out.println(age);

        
    }
}